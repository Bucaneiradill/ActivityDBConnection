    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        ContatoDAO remove = new ContatoDAO();
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        ContatoDAO dao = new ContatoDAO();
        int escolha;
        modelo.setNumRows(0);
        
        Contato c = dao.consultar(Integer.parseInt(txtId.getText()));
        String[] dados = new String[5];
        dados[0] = String.valueOf(c.getId());
        dados[1] = c.getNome();
        dados[2] = c.getTelefone();
        dados[3] = c.getEmail();
        dados[4] = c.getEndereco();
        modelo.addRow(dados);
        
        escolha = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o contato?");
        if(escolha == JOptionPane.YES_OPTION)
            remove.Exluir(Integer.parseInt(txtId.getText()));
    }//GEN-LAST:event_btnExcluirActionPerformed

